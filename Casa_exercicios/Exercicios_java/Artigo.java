package ExerciciosPOO;

public class Artigo {

	private double preco;
	public String nome;
	
	
	public Artigo() {

		
	}
	
	
	
	public String getNome(){
		return this.nome;
	}
	
	public void setNome(String n){
		this.nome = n;
	}
	
	public double getPreco(){
		return this.preco;
	}
	
	public void setPreco(double p){
		this.preco = p;
	}
	
	

}
